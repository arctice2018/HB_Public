# 在其他编程语言中的等号'='。c() 是一个
# 泛型函数，它结合参数，在这种情况下
# 是人的名字，形成一个向量。

# ‘矢量’ 是 R 中的数据类型之一。矢量必须包含
# 相同类型的数据，即条目必须全部是
# 相同类型：字符（大多数程序员称这些字符串），
# 逻辑（TRUE 或 FALSE）或数字。

